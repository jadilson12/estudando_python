n = input('Digite algo: ')
print('Esse pritivo desse valor é: ',type(n))
print('E alfabeto e cnumerico', n.isalnum())
print('E Alfabeto', n.isalpha())
print('E Decimal', n.isdecimal())
print('E digito', n.isdigit())
print('Ele é denticado ', n.isidentifier())
print('E minuscula ', n.islower())
print('imprimível ', n.isprintable())
print('spaço', n.isspace())
print('capitalizada', n.istitle())
print('é Mauscula', n.isupper())